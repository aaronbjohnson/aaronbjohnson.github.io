function generateGridHTML(grid) {
  let html = "<table>";
  for (let i = 0; i < grid.length; i++) {
    html += "<tr>";
    for (let j = 0; j < grid[i].length; j++) {
      html += `<td class="grid-cell" style="background-color:${grid[i][j]}"></td>`;
    }
    html += "</tr>";
  }
  html += "</table>";
  return html;
}


